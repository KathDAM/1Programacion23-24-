    private void actualizarTabla(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_actualizarTabla
        // TODO add your handling code here:
        int selectedRow = jTable1.getSelectedRow();
        if (selectedRow >= 0) {
            String codigoOficina = (String) tableModel.getValueAt(selectedRow, 0);
            String ciudad = (String) tableModel.getValueAt(selectedRow, 1);
            String pais = (String) tableModel.getValueAt(selectedRow, 2);
            String region = (String) tableModel.getValueAt(selectedRow, 3);
            String codigoPostal = (String) tableModel.getValueAt(selectedRow, 4);
            String telefono = (String) tableModel.getValueAt(selectedRow, 5);
            String direccion1 = (String) tableModel.getValueAt(selectedRow, 6);
            String direccion2 = (String) tableModel.getValueAt(selectedRow, 7);

            Oficina oficina = new Oficina(codigoOficina, ciudad, pais, region, codigoPostal, telefono, direccion1, direccion2);

            try {
                if (oficinaDAO.actualizarOficina(oficina)) {
                    JOptionPane.showMessageDialog(this, "Oficina actualizada con √©xito", "√âxito", JOptionPane.INFORMATION_MESSAGE);
                    cargarDatos();
                } else {
                    JOptionPane.showMessageDialog(this, "No se pudo actualizar la oficina", "Error", JOptionPane.ERROR_MESSAGE);
                }
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(this, "Error al actualizar la oficina: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(this, "Selecciona una oficina para actualizar", "Advertencia", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_actualizarTabla

//carga datos para actualizar la tabla
    private void cargarDatos() {
        try {
            List<Oficina> oficinas = oficinaDAO.obtenerTodasLasOficinas();
            tableModel = (DefaultTableModel) jTable1.getModel();
            tableModel.setRowCount(0); // Clear the table

            for (Oficina oficina : oficinas) {
                tableModel.addRow(new Object[]{
                        oficina.getCodigoOficina(),
                        oficina.getCiudad(),
                        oficina.getPais(),
                        oficina.getRegion(),
                        oficina.getCodigoPostal(),
                        oficina.getTelefono(),
                        oficina.getLineaDireccion1(),
                        oficina.getLineaDireccion2()
                });
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Error al cargar los datos: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }

    
